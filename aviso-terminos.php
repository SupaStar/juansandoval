            <h1 style="color: rgb(225,178,24);font-family: Thasadith, sans-serif;">Derecho Hipotecario</h1>
                <h2 style="color: rgb(0,56,105);font-family: Thasadith, sans-serif;font-size: 20px;text-align: justify;">Conjunto de normas del Derecho civil que regulan la inscripción en el Registro de la Propiedad y publicidad de los actos de reconocimiento, transmisión, constitución, modificación, gravamen y extinción del dominio sobre bienes inmuebles y derechos reales sobre los mismos.<br></h2>
                <h2 style="color: rgb(0,0,0);font-family: Thasadith, sans-serif;font-size: 15px;text-align: justify;">Es la parte del Derecho que estudia la adquisición, transmisión, modificación y extinción de los derechos sobre bienes inmuebles en cuanto éstos se reflejan en el Registro de la propiedad. Por lo tanto, y en cierto sentido, el Derecho hipotecario podría denominarse Derecho inmobiliario, en cuanto rama jurídica dedicada a las normas sobre<br>inmuebles. Asimismo, y en otro sentido, podría el Derecho hipotecario denominarse Derecho registral, en cuanto rama jurídica especializada en el dinamismo de los derechos sobre inmuebles reflejado en el referido registro. La denominación Derecho inmobiliario registral, posiblemente la más adecuada, ha sido muchas veces postergada ante la de Derecho hipotecario, de más rancia tradición al enlazar con el nombre de la ley especial que, antes de la promulgación del vigente Código civil, desmarcó de éste la materia inmobiliaria registral, sin perjuicio de que ésta pueda considerarse como rama del tronco común del Derecho civil.<br><br>El Derecho inmobiliario es el conjunto de normas y leyes que regula todas las operaciones que pueden realizarse sobre los bienes que la ley define como raíces -aquellos que por sus características físicas no pueden desplazarse del lugar en que se encuentran-, tanto entre ciudadanos como entre un particular y la administración pública. <br>Es decir, se encarga de regular todas las transacciones inmobiliarias: la forma en que se crean, transmite, modifican, adquieren o extinguen los derechos que una persona física o jurídica tiene sobre una propiedad inmueble o parte de ella, así como de los impuestos y gravámenes que debe hacer frente por cada una de estas operaciones. También abarca los aspectos referentes a la gestión de estos inmuebles, es decir, licencias urbanas, de obras o las normas que regulan las comunidades de vecinos.<br></h2>
                <h2 style="color: rgb(0,0,0);font-family: Thasadith, sans-serif;font-size: 15px;text-align: justify;">Como ocurre con otras ramas jurídicas, el Derecho inmobiliario es una materia transversal y su regulación se realiza a través de varias leyes que corresponden a distintas ramas del Derecho. Así, partiendo de la Constitución Española como marco jurídico, encontraremos que los derechos y deberes que afectan a las personas con derechos de cualquier tipo sobre un bien inmueble están regulados en distintos cuerpos legales: desde el Código Civil al Código Mercantil cuando sea&nbsp;de&nbsp;aplicación, además de leyes específicas como la Ley Hipotecaria o leyes tributarias que determinen los impuestos o gravámenes que recaen&nbsp;sobre un inmueble (IBI, Impuesto de Transmisiones Patrimoniales y Actos Jurídicos Documentados, etc.).&nbsp;</h2>
                <h2 style="color: rgb(0,0,0);font-family: Thasadith, sans-serif;font-size: 15px;text-align: justify;"> El ámbito de aplicación del Derecho inmobiliario, por tanto, será la regulación de todos aquellos actos y contratos que afecten a un bien inmueble. Así, en general, el Derecho inmobiliario aborda:<br></h2>
                <h2 style="color: rgb(0,0,0);font-family: Thasadith, sans-serif;font-size: 15px;text-align: justify;padding-right: 15px;padding-left: 15px;"><label style="color: rgb(225,178,24);height: 13px;">La regulación de los derechos reales sobre un bien inmueble&nbsp;<br></label>Se dividen básicamente en derecho de propiedad y derechos limitativos del dominio. El Derecho inmobiliario regula qué es la propiedad y los derechos que otorga, cómo se adquiere, se comparte o se transmite. En cuanto a los derechos limitativos, regula situaciones como el usufructo,&nbsp;la habitación o la servidumbre de un bien.<br></h2>
                <h2 style="color: rgb(0,0,0);font-family: Thasadith, sans-serif;font-size: 15px;text-align: justify;padding-right: 15px;padding-left: 15px;"><label style="color: rgb(225,178,24);height: 12px;"><strong>La compraventa de inmuebles&nbsp;u operaciones inmobiliarias.&nbsp;</strong><br></label>Este proceso requiere cumplir trámites previos para garantizar la validez de la operación: será necesario comprobar la situación urbanística, registral y administrativa de una finca antes de su&nbsp;compraventa, preparar contratos previos -como el de arras o el de opción de compra-, o liquidar los impuestos que devenguen antes y después de la compraventa.<br></h2>
                <h2 style="color: rgb(0,0,0);font-family: Thasadith, sans-serif;font-size: 15px;text-align: justify;padding-right: 15px;padding-left: 15px;"><label style="color: rgb(225,178,24);height: 12px;">La compraventa de inmuebles u operaciones inmobiliarias.&nbsp;<br></label>&nbsp;En el caso de un edificio en el que la propiedad de los pisos y locales corresponde a distintas personas, será necesario constituir una comunidad para tomar las decisiones pertinentes y cumplir las obligaciones legales.<br></h2>
